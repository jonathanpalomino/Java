    private void AsignarArreglo() {
        ASIGNAR_DATOS_EN_ARREGLO();
        GENERACION_VENTANA();
    }

    private void ASIGNAR_DATOS_EN_ARREGLO() {

        for (int j = 0; j < getMiModelo().getRowCount(); j++) {
            Object[] fila = new Object[3];
            for (int k = 0; k < getMiModelo().getColumnCount(); k++) {
                fila[k] = getMiModelo().getValueAt(j, k);
                //System.out.println("..." + fila[k]);
            }
            promedio += Double.parseDouble(fila[1].toString()) * Double.parseDouble(fila[2].toString());
            Ingresa_Datos_ESTRUCTURA_DINAMICA(fila[0], fila[1], fila[2], Double.parseDouble(fila[1].toString()) * Double.parseDouble(fila[2].toString()));
            ldco.agrega(escal);
        }
    }

    private void Ingresa_Datos_ESTRUCTURA_DINAMICA(Object Articulo, Object Req_anual, Object Precio, double multi) {
        escal = new Productos();
        escal.setPrecio(Double.parseDouble(Precio.toString()));
        escal.setProducto(Articulo.toString());
        escal.setReq_Anual(Double.parseDouble(Req_anual.toString()));
        escal.setmultiplo(multi);
    }

    private void GENERACION_VENTANA() {
        Resultados_Existencias objeto = new Resultados_Existencias(ldco, promedio);
        objeto.show();
    }

    
